public class SpecialChracters {

    public static void main(String[] args) {
        System.out.println("Single quotation mark: \'");
        System.out.println("Double quatation mark: \"");
        System.out.println("Backslash: \\");
        System.out.println("Tab:a \tb");            // \t
        System.out.println("Backspace:a \bb");      // \b
        System.out.println("Carriage return:a \rb");// \r
        System.out.println("Formfeed:a \fb");       // \f
        System.out.println("Newline:a \nb");        // \n

        System.out.println("A box:");
        System.out.println("-----------------");
        System.out.println("|\t\t|");
        System.out.println("|\t\t|");
        System.out.println("|\t\t|");
        System.out.println("|\t\t|");
        System.out.println("-----------------");

        System.out.print("print");
        System.out.print("\nprint");
        System.out.println("println");

        System.out.println("asdfffffffffffffffffffffffffffffx \r");
    }
}
