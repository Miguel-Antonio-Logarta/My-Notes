package pkg05_Code;

public class Loop_while {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 7) {
            System.out.println(i + " sheep");
            i++;
        }
    }
}

/* OUTPUT
1 sheep
2 sheep
3 sheep
4 sheep
5 sheep
6 sheep
7 sheep

 */